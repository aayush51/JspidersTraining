class Switch1 
{
	public static void main(String[] args) 
	{
		switch(3)
		{
			case 1 :
			{
				System.out.println("From 1");
				break;
			}
			case 2 :
			{
				System.out.println("From 2");
				break;
			}
			case 3 :
			{
				System.out.println("From 3");
			}
			case 4 :
			{
				System.out.println("From 4");
			}
			default:
			{
				System.out.println("From Default");
			}
		}
	}
}
